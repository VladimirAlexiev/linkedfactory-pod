package io.github.linkedfactory.kvin.parquet;

public class SimpleMapping implements IdMapping {
	long id;
	String value;

	@Override
	public long getId() {
		return id;
	}

	@Override
	public void setId(long id) {
		this.id = id;
	}

	@Override
	public String getValue() {
		return value;
	}

	@Override
	public void setValue(String value) {
		this.value = value;
	}
}